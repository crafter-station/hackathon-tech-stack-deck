import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const slidesDir = path.join(root, "src/content/slides");
const outputDir = path.join(root, "src/generated");
const outputFile = path.join(outputDir, "slides-manifest.ts");

function pickExportBlock(source, exportName) {
  const pattern = new RegExp(
    `export\\s+const\\s+${exportName}\\s*=\\s*(\\[[\\s\\S]*?\\]|\\{[\\s\\S]*?\\})\\s*;`,
  );
  const match = source.match(pattern);

  if (!match) {
    throw new Error(`Missing export '${exportName}'`);
  }

  return match[1];
}

async function main() {
  const fileNames = (await readdir(slidesDir))
    .filter((fileName) => fileName.endsWith(".mdx"))
    .sort((left, right) => left.localeCompare(right));

  const slides = [];

  for (const fileName of fileNames) {
    const absolutePath = path.join(slidesDir, fileName);
    const source = await readFile(absolutePath, "utf8");
    const metaBlock = pickExportBlock(source, "meta");
    const notesBlock = pickExportBlock(source, "notes");
    const referencesBlock = source.includes("export const references")
      ? pickExportBlock(source, "references")
      : undefined;

    const meta = Function(`"use strict"; return (${metaBlock});`)();
    const notes = Function(`"use strict"; return (${notesBlock});`)();
    const references = referencesBlock
      ? Function(`"use strict"; return (${referencesBlock});`)()
      : [];

    if (!meta?.slug || !meta?.title) {
      throw new Error(`Slide ${fileName} must export meta.slug and meta.title`);
    }

    if (!Array.isArray(notes)) {
      throw new Error(`Slide ${fileName} must export notes as an array`);
    }

    if (!Array.isArray(references)) {
      throw new Error(
        `Slide ${fileName} must export references as an array when provided`,
      );
    }

    slides.push({
      fileName,
      importPath: `@/content/slides/${fileName}`,
      meta,
      notes,
      references,
    });
  }

  const componentImports = slides
    .map(
      (slide, index) =>
        `import Slide${index + 1} from ${JSON.stringify(slide.importPath)};`,
    )
    .join("\n");

  const records = slides
    .map(
      (slide, index) => `  {
    fileName: ${JSON.stringify(slide.fileName)},
    href: ${JSON.stringify(`/${slide.meta.slug}`)},
    index: ${index},
    meta: ${JSON.stringify(slide.meta, null, 4).replaceAll("\n", "\n    ")},
    notes: ${JSON.stringify(slide.notes, null, 4).replaceAll("\n", "\n    ")},
    references: ${JSON.stringify(slide.references, null, 4).replaceAll("\n", "\n    ")},
    Component: Slide${index + 1},
  }`,
    )
    .join(",\n");

  const contents = `/* eslint-disable */
/* auto-generated by scripts/generate-slides-manifest.mjs */

import type { ComponentType } from "react";

${componentImports}

export type SlideTheme = "dark" | "light";

export type SlideMeta = {
  slug: string;
  title: string;
  theme?: SlideTheme;
  kicker?: string;
  accent?: string;
};

export type SlideReference = {
  label: string;
  href: string;
};

export type SlideRecord = {
  fileName: string;
  href: string;
  index: number;
  meta: SlideMeta;
  notes: string[];
  references: SlideReference[];
  Component: ComponentType;
};

export const slideRecords: SlideRecord[] = [
${records}
];
`;

  await mkdir(outputDir, { recursive: true });
  await writeFile(outputFile, contents);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
